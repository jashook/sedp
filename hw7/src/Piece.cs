class Piece : Cell
{
   // Member Variables

   public int Team { get; set; }

   public Piece() : base(0,0)
   {
      Team = 0;
   }

   public Piece(int x, int y, int team) : base(x,y)
   {
      Team = team;
   }

   public virtual bool Occupied()
   {
      return true;
   }

} // end of class(Point)

